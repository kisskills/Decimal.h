Suite *random_suite(void) {
    Suite *s;
    TCase *tc_core;

    s = suite_create("decimal_change");
    tc_core = tcase_create("Core");

    tcase_add_test(tc_core, test_add);
    tcase_add_test(tc_core, test_sub);
    tcase_add_test(tc_core, test_mul);
    tcase_add_test(tc_core, test_div);
    tcase_add_test(tc_core, test_mod);

    tcase_add_test(tc_core, test_is_equal);
    tcase_add_test(tc_core, test_is_not_equal);
    tcase_add_test(tc_core, test_is_less);
    tcase_add_test(tc_core, test_is_less_or_equal);
    tcase_add_test(tc_core, test_is_greater);
    tcase_add_test(tc_core, test_is_greater_or_equal);

    tcase_add_test(tc_core, test_truncate);
    tcase_add_test(tc_core, test_floor);
    tcase_add_test(tc_core, test_negate);
    tcase_add_test(tc_core, test_round);

    tcase_add_test(tc_core, test_from_int_to_decimal);
    tcase_add_test(tc_core, test_from_decimal_to_int);
    tcase_add_test(tc_core, test_from_float_to_decimal);
    tcase_add_test(tc_core, test_from_decimal_to_float);

    suite_add_tcase(s, tc_core);

    return s;
}

int main(void) {
    int no_failed = 0;
    Suite *s;
    SRunner *runner;

    s = random_suite();
    runner = srunner_create(s);

    srunner_run_all(runner, CK_NORMAL);
    no_failed = srunner_ntests_failed(runner);
    srunner_free(runner);
    return (no_failed == 0) ? EXIT_SUCCESS : EXIT_FAILURE;
}
